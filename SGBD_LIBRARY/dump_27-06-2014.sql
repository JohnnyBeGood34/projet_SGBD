----------------------

  CREATE OR REPLACE TRIGGER "BD_MM"."DISPO_SITE_ADHESION" 
BEFORE INSERT ON ADHERER
FOR EACH ROW
DECLARE 
nbTotalMateriel NUMBER;
nbExemplairesLoues NUMBER;
nbExemplairesAchats NUMBER;
BEGIN
  SELECT COUNT(*) INTO nbExemplairesLoues 
  FROM EXEMPLAIREMATERIELLOUE WHERE IDSITEEXEMPLAIRE=:new.IDSITE;
  
  SELECT COUNT(*) INTO nbExemplairesAchats
  FROM EXEMPLAIREMATERIELACHAT WHERE IDSITEEXEMPLAIRE =:new.IDSITE;
  
  nbTotalMateriel := nbExemplairesLoues + nbExemplairesAchats;
  
  DBMS_OUTPUT.PUT_LINE('Stock du site ' || :new.IDSITE || ' :' || nbTotalMateriel);
END;
