qteMateriel NUMBER;

/*Curseur qui récupère toutes les commandes en attente ordonnées 
de la plus ancienne à la plus récente pour les prioriser par date.
*/
CURSOR commandes_attente IS
SELECT * FROM COMMANDEENATTENTEACHAT ORDER BY DATECOMMANDE ASC;
/*Permet de commiter dasn le trigger */
pragma autonomous_transaction;


BEGIN
   /*Pour chaque commande en attente*/
    FOR commande IN commandes_attente

    LOOP
       /*On prends la quantité du matériel qui est concerné par cette commande en attente */
        SELECT MAX(count(idmaterielmedical)) INTO qteMateriel
        FROM EXEMPLAIREMATERIELACHAT where idmaterielmedical=commande.idmaterielachat group by IDSITEEXEMPLAIRE;
       
       /*Si la quantité de la commande en attente est plus petite ou égale
       à la quantité de ce matériel demandé , on peut le fournir
       */
  
       
            IF commande.qte <= qteMateriel
                THEN
                /*On enregistre la ligne de la commande */
                INSERT INTO CONCERNERCOMMANDEACHAT VALUES (commande.idmaterielachat,commande.idcommande,commande.qte,null);
                /*Et on supprime la commande en attente pour ce matériel*/
                DELETE COMMANDEENATTENTEACHAT where IDCOMMANDEENATTENTEACHAT=commande.idcommandeenattenteachat;
                COMMIT;
            END IF;
